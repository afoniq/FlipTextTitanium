function flipString(s) { 
    var result = new Array(s.length); 
    for (var i = s.length - 1; i >= 0; --i) { 
            var o = s.charAt(i); 
            var f = flipTable[o]; 
            result[s.length - 1 - i] = f != undefined ? f : o; 
            } 
    return result.join(''); 
    } 
